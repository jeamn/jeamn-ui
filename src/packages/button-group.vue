<template>
  <div class="jeamn-button-group">
    <slot></slot>
  </div>
</template>

<script>
export default {
  name: 'jeamn-button-group',
  mounted(){
    // 校验孩子节点是否是button
    let children = this.$el.children
    for(let i=0; i<children.length; i++){
      console.assert(children[i].tagName === 'BUTTON', '子元素必须是button')
    }
  }
}
</script>

